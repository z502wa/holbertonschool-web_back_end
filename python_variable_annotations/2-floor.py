#!/usr/bin/env python3
"""
Module: 2-floor
This module provides a function to compute the floor of a float.
"""

import math


def floor(n: float) -> int:
    """
    Return the floor of the given float.

    Args:
        n (float): The number to floor.

    Returns:
        int: The floor value of n.
    """
    # Use math.floor to compute the floor of n
    return math.floor(n)

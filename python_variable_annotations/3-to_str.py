#!/usr/bin/env python3
"""
Module: 3-to_str
This module provides a function to convert a float to its string
representation.
"""


def to_str(n: float) -> str:
    """
    Convert a float to a string.

    Args:
        n (float): The number to convert.

    Returns:
        str: The string representation of n.
    """
    # Convert the float to string and return it
    return str(n)
